    // Verifica se o usuário está registrado
    private boolean verificarUsuario(String email, String senha) {
        DB db = new DB("bancodedados.db");
        System.out.println(email);
        System.out.println(senha);
        String query = "SELECT * FROM tb_funcionarios";
        query = query + " WHERE";
        query = query + " email = '" + email + "'";
        query = query + " AND ";
        query = query + " senha = '" + senha + "';";
        db.query("SELECT * FROM tb_funcionarios WHERE email= '" + email + "' AND senha= '" + senha + "';" );
              
        if (db.next()) {
            // Usuário encontrado, feche a conexão e retorne true
            db.closeConnection();
            this.dispose();
            return true;
        }

        // Usuário não encontrado, feche a conexão e retorne false
        db.closeConnection();
        return false;
    }

